	/**
	 * 显示软件批处理视图。
	 */
	public static void showPanel()
	{
		net.infonode.docking.View view = SingleFrameApplication.getInstance().getView(Constants.VIEW_CCFK);
		if(view == null)
		{
			CcfkPanel sbv = new CcfkPanel();
			view = SingleFrameApplication.getInstance().addDynamicView(Constants.VIEW_CCFK,
					sbv.resourceMap.getString("dynamicview.ccfkPanel"),
					sbv.resourceMap.getIcon("image.ccfk"),
					sbv);
			view.addListener(sbv.viewListener);
		}
		else
		{
			view.restoreFocus();
		}
	}

