	private class CcfkViewListener extends DockingWindowAdapter
	{
		@Override
		public void windowClosed(DockingWindow window)
		{
			// 清除资源
			clear();
			// 删除视图事件监听器
			window.removeListener(viewListener);
			viewListener = null;
		}
	}
